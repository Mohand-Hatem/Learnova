@Injectable({ providedIn: 'root' })
export class CompanyService {
  getCompanies(): Observable<Company[]> {
    return of(MOCK_COMPANIES);
  }

  getCompanyById(id: string): Observable<Company | undefined> {
    return of(MOCK_COMPANIES.find(c => c.id === id));
  }

  updateStatus(id: string, status: CompanyStatus): Observable<void> {
    const company = MOCK_COMPANIES.find(c => c.id === id);
    if (company) company.status = status;
    return of(void 0);
  }

  deleteCompany(id: string): Observable<void> {
    const idx = MOCK_COMPANIES.findIndex(c => c.id === id);
    if (idx !== -1) MOCK_COMPANIES.splice(idx, 1);
    return of(void 0);
  }
}
